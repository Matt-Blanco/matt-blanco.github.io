function parseData(str) {
    let arr = str.split('\n');
    let jsonObj = [];
    let headers = arr[0].split(',');
    for (let i = 1; i < arr.length; i++) {
        let data = arr[i].split(',');
        let obj = {};
        for (let j = 0; j < data.length; j++) {
            obj[headers[j].trim()] = data[j].trim();
        }
        jsonObj.push(obj);
    }
    return jsonObj
}

